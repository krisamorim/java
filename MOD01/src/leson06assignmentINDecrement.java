public class leson06assignmentINDecrement {
    public static void main(String[] args){
        double numberD = 1.800D;
        numberD += 10; //means numberD = numberD + 10
        numberD -= 10; //means numberD = numberD - 10
        numberD *= 2; //means numberD = numberD * 2
        System.out.println(numberD);

        int onePlusone = 0;
        System.out.println(onePlusone++);
        System.out.println(++onePlusone);
    }
}
