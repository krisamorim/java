public class leson04TypesArray {
    public static void main(String[] args) {
        //CASE1
        int[] age = new int[3]; //int
        age[0] = 10;
        age[1] = 20;
        age[2] = 30;
        System.out.println(age[0]);

        //CASE2
        float[] numberArray2 = new float[]{10,20,30};//float
        System.out.println(numberArray2[0]);

        //CASE3
        float[] numberArray3 = {10,20,30};//float
        System.out.println(numberArray2[0]);

    }
}
