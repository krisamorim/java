public class leson04TypesArray {
    public static void main(String[] args) {
        int[] age = new int[3]; //int
        age[0] = 10;
        age[1] = 20;
        age[2] = 30;
        System.out.println(age[0]);

        float[] numberArray2 = new float[3];//float
        numberArray2[0] = 10;
        numberArray2[1] = 20;
        numberArray2[2] = 30;
        System.out.println(numberArray2[0]);
    }
}
